          中国移动、阿里云、腾讯云、百度云、京东云、华为云、网宿科技要对互联网电视业务相关的内容分发网络(CDN)进行IPv6改造。2020年末，基于IPv6协议的互联网电视业务服务能力达到IPv4的85%以上；基于IPv6协议的互联网电视业务加速性能达到IPv4的85%以上。
        </p>
        <p>(三)着力提升接收设备IPv6支持能力
        </p>
        <p>小米、华为、创维、海信、TCL、长虹、康佳、九州等厂商加快实现新生产的互联网电视接收设备(含终端ROM、播放器、应用服务框架、终端管理模块等)支持IPv6，出厂默认配置支持IPv4/IPv6双栈；加快对具备条件的存量互联网电视接收设备通过固件及系统升级等方式支持IPv6。天猫、京东、淘宝、拼多多等电子商务平台企业要优先向用户推荐支持IPv6的机顶盒、智能电视机等终端设备。
          　　各相关单位新部署的互联网电视接收设备(包括但不限于机顶盒、智能电视机等产品形态)应支持IPv6，加快对具备条件的存量互联网电视接收设备通过系统软件升级等方式支持IPv6.2020年三季度末，各相关单位为全部具备条件的存量互联网电视接收设备完成升级。
          　</p>

        <p>二、保障措施
        </p>

        <p>(一)严格落实责任。各相关单位要进一步细化任务分解，层层压实责任，安排专门资金，保障任务落实。要建立互联网电视业务IPv6改造任务清单，明确时间安排，每季度结束后的10个工作日内向工业和信息化部(信息通信发展司)、国家广播电视总局(科技司)报送季度工作进展情况。
        </p>

        <p>(二)加强对接协调。工业和信息化部、国家广播电视总局将聚焦IPv6改造过程中存在的困难和问题，进一步完善对接协调机制，及时推进解决。各相关单位要加强配合，及时报告IPv6改造过程中的困难和问题，确保完成年度任务。
        </p>

        <p>(三)完善标准规范。国家广播电视总局推动完善互联网电视集成平台标准体系。工业和信息化部尽快推动完善互联网电视接收设备相关国家标准和行业标准，在互联网电视接收设备有关报送系统中增加IPv6支持情况统计。
        </p>
        <p>(四)强化跟踪监测。中国信息通信研究院要加强对互联网电视集成平台、传输网络和接收设备相关IPv6监测评估能力建设，强化改造进展情况跟踪监测。各相关单位要积极配合，根据评估需要在关键环节上部署监测能力。工业和信息化部、国家广播电视总局将定期向各省、自治区、直辖市主管部门、各单位反馈通报。
        </p>
