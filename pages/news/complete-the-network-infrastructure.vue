        <p>通知全文如下：</p>
        <h3>工业和信息化部办公厅、国家广播电视总局办公厅关于推进互联网电视业务IPv6改造的通知</h3>
        <p>推进互联网电视业务IPv6改造是落实中共中央办公厅、国务院办公厅《推进互联网协议第六版(IPv6)规模部署行动计划》的重要举措，是促进我国固定宽带网络IPv6流量提升的关键手段。为进一步推进互联网电视集成服务平台、传输网络和接收设备的IPv6改造，现就有关事项通知如下
        </p>
        <h3> 一、重点任务及目标</h3>

        <p>(一)全面完成网络基础设施IPv6升级改造 </p>
