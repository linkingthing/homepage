        <p>工信部办公厅、国家广播电视总局办公厅发布关于推进互联网电视业务IPv6改造的通知。通知要求，中国电信、中国移动、中国联通要对互联网电视业务经过的骨干网、城域网、接入网以及互联网骨干直联点相关设备进行IPv6改造。2020年三季度末，上述网络基础设施IPv6改造全面完成，IPv6网络性能与IPv4趋同，平均丢包率、时延等主要指标与IPv4相比劣化不超过10%。
