            各单位、部属各高校、基础电信企业继续深化门户网站 IPv6 改造，到 2020 年末，门户网
            站二级、三级链接的 IPv6 浓度达到 85%以上。
            基础电信企业集团及下属省级公司稳步提升自营移动互联网应用（APP）的 IPv6 浓度，
            到 2020 年末，排名前 10 位的自营移动互联网应用（APP）的 IPv6 浓度达到 60%以上；
