            微服务引擎、MapReduce 服务、设备接入服务（IoT Hub）、区块链服务、视频直播、人
            脸识别等在内的全部公有云产品的 IPv6 改造；国内支持 IPv6 服务的可用域（Region）数
            量达到全部 IPv4 可用域的 50%以上。</li>
          <li>（四）全面扩大数据中心（IDC）IPv6 覆盖范围。中国电信、中国移动、中国联通进一
            步加强对中小型数据中心和机房的 IPv6 改造。阿里云、腾讯云、百度云、京东云、华为云、
            世纪互联、鹏博士、秦淮科技、新网互联、方正信息、西部数码、万国数据、光环新网加快
            数据中心 IPv6 改造力度，到 2020 年第三季度末，完成年报中全部数据中心的 IPv6 改造，
            形成全国范围数据中心 IPv6 覆盖能力。</li>
          <li>（五）着力提升终端设备 IPv6 支持能力。普联（TP-Link）、友讯（D-Link）、华为、
            中兴、烽火、上海贝尔、新华三、腾达、华硕、网件、小米新生产的家庭网关、企业网关、
            无线路由器、智能家居终端应默认配置支持 IPv4/IPv6 双栈。天猫、京东、淘宝等电商平台
            应优先向用户推荐支持 IPv6 的终端设备。基础电信企业加速存量家庭网关的更新替换，到
            2020 年末，完成对所有可远程升级家庭网关 IPv6 升级。</li>
