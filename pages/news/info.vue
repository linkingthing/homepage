            应用宝、360 手机助手、豌豆荚、OPPO 软件商店、百度手机助手、华为应用市场、
            小米应用商店、VIVO 应用商店、MM 商场、沃商店对新上架的 APP 开展 IPv6 浓度检测，
            并设立 IPv6 应用专区，引导用户安装支持 IPv6 的应用。相关企业加强对开发工具包（SDK）
            及服务器端程序的 IPv6 升级改造工作。</li>
          <li> （七）着力强化 IPv6 网络安全保障能力。各相关企业要进一步完善针对 IPv6 的网络安
            全定级备案、风险评估、通报预警、灾难备份及恢复等工作。推动 IPv6 环境下网络安全产
            品和服务的应用，鼓励构建 IPv6 安全产品孵化平台和测试环境，推动在研 IPv6 安全产品孵
            化，强化 IPv6 安全产品应用性能验证。基础电信企业和重点 IDC、CDN 等企业要做好僵木
            蠕、移动互联网恶意程序监测处置系统、信息安全管理系统等安全技术手段 IPv6 配套改造
            工作，强化 IPv6 环境下漏洞、违法信息等的监测发现与处置。</li>

        </ul>

        <h3> 二、2020 年末主要目标</h3>

        <ul>
          <li> （一）IPv6 网络性能与 IPv4 趋同，平均丢包率、时延、连接建立成功率等指标与 IPv4
            相比劣化不超过 10%。</li>
          <li> （二）IPv6 活跃连接数达到 11.5 亿。其中，中国电信集团有限公司达到 2.9 亿，中国
            移动通信集团有限公司达到 6.4 亿，中国联合网络通信集团有限公司达到 2.2 亿。</li>
          <li> （三）移动网络 IPv6 流量占比达到 10%以上。</li>

        </ul>

        <h3>三、保障措施</h3>

        <ul>
          <li>
            （一）压实工作责任。各企业要对照年度任务目标，进一步细化分解，做到“落实举措、
            责任主体、完成时限”三明确。各基础电信企业集团公司要加强对各省级子（分）公司的工
            作部署和指导，及时掌握工作进展，开展检查督导，并在年度考核中将 IPv6 相关任务完成
            情况作为重要指标。
          </li>
          <li>
            （二）强化协同攻坚。工业和信息化部（信息通信发展司）组织成立 IPv6 规模部署专
            项协同推进工作组，制定可量化、可考核的技术规范和标准，以 IPv6 端到端贯通为核心目
            标，建立任务台账、安排专人对接、逐项协调解决，保障 IPv6 升级改造工作顺利推进。
          </li>
          <li> （三）完善技术监测。中国信息通信研究院要会同相关企业和产业联盟持续完善 IPv6
            发展监测平台，开展 IPv6 端到端网络性能监测，内容分发网络（CDN）、云平台、行业移
            动互联网应用（APP）、行业网站等的 IPv6 支持程度监测等工作，定期发布 IPv6 发展监测
            报告。各相关企业要配合做好监测节点部署工作。

          </li>
          <li> （四）开展抽查抽测。各地通信管理局要瞄准重点任务、紧扣时间节点，通过明查暗访
