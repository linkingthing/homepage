            产品，在全国范围内为有需求的政企客户提供 IPv4/IPv6 双栈专线、IPv6 单栈专线、IPv6
            代播等多种业务。支持基础电信企业对 IPv6 单栈专线开通给予九五折或更大力度资费优惠。</li>
          <li>（二）加快提升内容分发网络（CDN）IPv6 应用加速能力。阿里云、腾讯云、网宿科
            技、蓝汛、金山云、百度云、华为云、京东云、帝联科技、UCloud、白山云、七牛云、鹏
            博士、中国移动加快内容分发网络 IPv6 深度改造，提升 IPv6 服务资源占比、扩大 IPv6 服
            务覆盖范围、优化 IPv6 应用加速性能。到 2020 年末，内容分发网络支持 IPv6 的节点数达
            到 IPv4 节点数的 85%以上；按地市级行政区划，IPv6 服务覆盖能力达到 IPv4 服务覆盖能
            力的 85%以上；IPv6 应用加速性能达到 IPv4 应用加速性能的 85%以上。</li>
