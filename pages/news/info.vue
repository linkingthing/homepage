        <p>为贯彻落实《推进互联网协议第六版(IPv6)规模部署行动计划》（厅字〔2017〕47 号）
          任务要求，加快提升 IPv6 端到端贯通能力，持续提升 IPv6 活跃用户和网络流量规模，我部
          决定于 2020 年开展 IPv6 端到端贯通能力提升专项行动。有关事项通知如下：</p>
        <h3>一、重点工作任务</h3>
        <ul>
          <li> （一）优化提升 IPv6 网络接入能力。基础电信企业持续优化骨干网、城域网、接入网
