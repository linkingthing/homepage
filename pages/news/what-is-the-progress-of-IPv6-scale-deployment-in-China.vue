        <h1>我国IPv6规模部署进展如何？ </h1>
        <p><time>发布时间： 2020-6-28</time>新闻出处： <a href="http://www.caict.ac.cn/kxyj/caictgd/202006/t20200628_285037.htm">中国信通院CATCT</a> </p>
