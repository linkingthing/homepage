      <div>
        <p>为加快推进上海市IPv6规模部署，促进基础网络及相关应用协同演进，推动互联网升级和健康创新发展，支撑新一代网络基础设施（“新网络”）建设行动，上海市委网信办、上海市发展改革委、上海市经济信息化委、上海市通信管理局近日联合印发《上海市推进互联网协议第六版（IPv6）规模部署三年行动计划（2020-2022年）》（以下简称《行动计划》），明确未来三年内上海市IPv6规模部署的总体考虑、主要任务和保障措施。通过三年的时间，统筹推进上海市IPv6发展，形成下一代互联网自主技术体系和产业生态，实现下一代互联网在经济社会各领域深度融合应用。
