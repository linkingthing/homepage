        <p>4.2 CDN质量</p>
        <p>4.2.1 测试方法</p>
        <p>CDN测试采用第三方基调网络测试系统对各运营商的网络状况进行检测，并通过全国不同地理位置分布的监测节点来模拟真实的用户访问。由于监测节点为私募机器，该机器会因网络出口或网络带宽等问题影响测试性能。各区域添加的测试点数将结合客户需求及该区域节点的实际情况，由基调系统随机抽取。</p>

        <p>最终数据是综合中国大陆5个LASTMILE测试区域随机测出的结果，由几个核心综合指标构成。该指标体系能从不同技术纬度定量反映网站的服务质量，可以更有针对性地帮助提高网站性能。</p>

        <ul>
          <li>（1）性能指标：平均响应时间，该值越小，则响应时间越短。</li>
          <li>（2）可靠性指标：访问成功率，该值越高，则可靠性越强。</li>
          <li>（3）DNS时间：通过域名解析服务（DNS），将指定的域名解析成IP地址的消耗时间。</li>
          <li>（4）建立连接时间：IE浏览器和Web服务器建立TCP/IP连接的消耗时间。</li>
          <li>（5）首包时间：从IE浏览器发送HTTP请求结束，到收到Web服务器返回的第一个数据包的消耗时间。</li>
          <li>（6）内容下载时间：监测一个页面时，从页面角度来看，IE浏览器接收Web服务器返回的非第一个数据包的消耗时间。</li>
        </ul>

