        <h3>1 引言</h3>

        <p>2017年11月，中共中央办公厅、国务院办公厅联合印发了《推进互联网协议第六版（IPv6）规模部署行动计划》（以下简称《行动计划》），明确提出未来5~10年我国基于IPv6的下一代互联网发展的总体目标、路线图、时间表和重点任务，这是加快推进我国IPv6规模部署、促进互联网演进升级和健康创新发展的行动指南[1]。</p>

        <p>《行动计划》发布以来，政府部门、中央企业、基础电信企业、互联网企业、通信设备制造企业、科研机构等积极响应，纷纷制定具体的落地实施方案和工作计划，加快IPv6升级改造，使我国IPv6规模部署工作呈现加速发展态势。</p>

        <p>据国家IPv6发展平台统计，截止到2019年年底，我国IPv6活跃用户已经接近3亿[2]。根据中国互联网络信息中心（CNNIC）第45次《中国互联网络发展状况统计报告》显示，我国网民规模为9.04亿[3]，IPv6用户占比已接近30%。</p>

        <h3>2 以IPv6为基础的下一代互联网将为“新基建”夯实基础</h3>

        <p>2020年4月20日，国家发展改革委员会首次明确了新型基础设施的范围。新型基础设施是以新发展理念为引领，以技术创新为驱动，以信息网络为基础，面向高质量发展需要，提供数字转型、智能升级、融合创新等服务的基础设施体系[4]。新型基础设施主要包括3方面内容。</p>

        <p>一是信息基础设施，包括以5G、物联网、工业互联网、卫星互联网为代表的通信网络基础设施，以人工智能、云计算、区块链等为代表的新技术基础设施，以数据中心、智能计算中心为代表的算力基础设施等。</p>

        <p>二是融合基础设施，主要指深度应用互联网、大数据、人工智能等技术，支撑传统基础设施转型升级，进而形成的融合基础设施，如智能交通基础设施、智慧能源基础设施等。</p>

        <p>三是创新基础设施，主要指支撑科学研究、技术开发、产品研制的具有公益属性的基础设施，如重大科技基础设施、科教基础设施、产业技术创新基础设施等。</p>

        <p>相比传统基建，科技创新、数字化、信息网络这3个要素是所有关于新基建认知中的最大公约数，也是中国下一步经济发展的主要路径。</p>

        <p>从人与人之间的连接走向万物互联，所需的元器件整体数量更多，产生的数据更大，对时延等要求也更高。工业互联网、物联网的特性决定了其需要使用大量的IP地址。IPv6使得每一个单元都可以拥有独立的IP地址，使工业互联网和物联网得以落地。因此，IPv6成为支撑信息基础设施的基础。</p>

        <h3>3 网络基本实现IPv6“可用”</h3>

        <p>3.1 基础电信企业基本完成网络改造</p>
        <p>3.1.1 LTE网络和固定网络IPv6升级改造全面完成</p>
        <p>截至2020年1月，全国已有11.93 亿LTE用户、1.99 亿固定网络用户获得了IPv6地址。</p>

        <p>3.1.2 骨干直联点实现IPv6互联互通</p>
        <p>2019年，武汉、西安、沈阳、南京、重庆、杭州、贵阳·贵安、福州8个互联网骨干直联点全部完成了IPv6升级改造，支持互联网网间IPv6流量交换，加上2018年完成的北京、上海、广州、郑州、成都5个骨干网直联点，我国13个骨干网直联点全部完成了IPv6改造。</p>

        <p>3.1.3 基础电信企业IDC全面完成IPv6升级改造</p>
        <p>中国电信、中国移动、中国联通等基础电信企业已完成全部907个超大型、大型、中小型数据中心（IDC）的IPv6改造。</p>

        <p>3.1.4 内容分发网络资源对IPv6的支持能力持续提升</p>
        <p>2020年1月，网宿科技、阿里云、腾讯云、蓝汛、金山云、世纪互联、UCloud、白山云、七牛云、中国移动等主要CDN企业支持IPv6的节点数超过3195个。按照省级行政区划计算，IPv6全国覆盖能力达到99%以上，IPv6本地覆盖能力达到IPv4本地覆盖能力的85%以上，基本具备IPv6分发加速能力。</p>

        <p>3.2 网站及应用积极稳妥推进IPv6升级</p>
        <p>截止到2020年1月，全国91家省部级以上政府网站和96家央企网站中已有165家网站的首页可通过IPv6访问，支持率为88.2%。</p>

        <p>中国电信、中国移动、中国联通发挥运营商的自身优势，2019年除了完成门户网站、网上营业厅IPv6改造外，还分别完成了掌上营业厅、邮箱等自营业务排名前10的移动互联网应用（APP）的IPv6深度改造。根据最新监测数据，基础电信企业30款自营移动互联网应用平均IPv6流量占比超过71%。</p>

        <p>在用户量排名前100的互联网应用中，实现IPv6全国上线的有41个，上线地区超过半数的有12个、小于半数的有29个、未上线的18个。</p>

        <h3>4 从网络“可用”到网络“好用”</h3>

        <p>网络质量是网络“好用” 的基础。IPv6网络质量包括端到端网络质量和CDN质量两个方面。2020年年初进行的IPv6网络质量测试结果显示，相比2019年，2020年IPv6网络质量已经有了显著的改善。</p>

        <p>4.1 端到端网络质量</p>
        <p>4.1.1 测试方法</p>
        <p>测试采取“埋点” 的方法，测试端到端的网络指标，主要包括连接成功率和时延，连接成功率可以体现网络是否真正完成了改造，而时延则反映了整个网络的性能。</p>

        <p>4.1.2 测试结果</p>
        <p>在全国范围随机抽取10个省市，对某头部互联网应用进行测试，结果显示：大部分省市IPv6连接成功率要高于IPv4，且IPv6 时延低于IPv4，具体参见表 1。</p>

