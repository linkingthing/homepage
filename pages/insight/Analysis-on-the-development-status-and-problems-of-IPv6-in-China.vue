        <h3>5 存在的问题和挑战</h3>

        <p>IPv6已得到行业的广泛重视，但从 “可用”到“好用”，再从“好用”到“爱用”，仍然存在很多问题和挑战。</p>

        <p>5.1 活跃用户增长幅度与流量占比不匹配</p>
        <p>目前，我国已有2亿左右的IPv6活跃用户，但IPv6流量占比仍偏低，LTE网络IPv6流量仅为IPv4的5%左右，城域网流量占比不足3%。正常来说，在改造和放量期，活跃用户占比与流量占比偏差不应超过50%；全量后，该偏差不应超过30%。</p>

        <p>5.2 家庭无线路由器的支持率低限制了大流量的功能</p>
        <p>家庭无线路由器IPv6支持率偏低，已经成为整个IPv6规模发展的最大瓶颈。网络接入的“ 最后一公里”无法支持IPv6，无法带动视频、游戏这些大流量的提升。为了解决此问题，工业和信息化部在2020年的工作计划中明确要求，家庭无线路由器厂商不仅要对存量终端进行升级，还要对新上市的终端默认支持IPv6功能。</p>

        <p>5.3 安全挑战</p>
        <p>相对于IPv4网络，IPv6在组网架构、服务提供方式上有较大的变化，这些变化也对IPv6的安全提出了更大的挑战。例如，在云产品中，原来用于IPv4的许多安全解决方案可能不再适用于IPv6，此外也涉及到网络、业务和用户数据、用户隐私管理和控制等方面的安全问题。</p>

        <p>5.4 产业生态尚需培育</p>
        <p>多年来，由于IPv4地址短缺，我国互联网的发展一直深陷“网络地址转换（NAT）” 技术泥潭中。很多企业出于自身成本利益的考虑，对切换至IPv6持观望态度，相关厂商在IPv6领域中独自摸索。产业生态尚未成熟，业界对IPv6未来发展的理解不尽相同。IPv6的最终目的是促进各行业的数字化转型，全面支持“新基建”，所以其不仅需要全面构建面向垂直行业个性需求的多元化平台，还需要进行广泛的跨界合作。</p>

        <h3>6 发展建议</h3>

        <p>6.1 加强家庭无线路由器的IPv6支持</p>
        <p>家庭无线路由器的IPv6支持两个方面：一是路由器要和上游运营商提供的智能家庭网关匹配，为用户提供IPv6接入能力；另一方面，可以通过自动推送固件的方式升级，避免繁琐的设置。</p>

        <p>目前，对于家庭无线路由器如何支持IPv6并没有统一的技术规范，企业都是各自为战。建议相关部门尽快出台技术规范，以标准化为手段引导和促进产业链的高效合作。标准化是一个系统工程，需要权威机构进行统一规划，为标准的制定提供指导思路；对已经应用的关键技术，及时进行标准化，确保互操作性和连续性。</p>

        <p>6.2 完善IPv6相关安全机制</p>
        <p>网络与信息安全是推进IPv6健康发展的前提，有效的安全机制可以避免IPv6引入对运营商基本网络和互联网应用服务的不良影响，从技术和管理上切实保障安全，扫除互联网应用的顾虑。“ 新基建” 的提出，对安全和可靠性提出了更高的要求，需要迅速完善相关的安全产品，保障其安全性。</p>

        <p>6.3 构建开放共赢的产业生态</p>
        <p>IPv6不仅是网络技术的创新，同时也是商业模式的创新。5G、物联网、工业互联网除了技术与场景、网络与应用的融合，还有生态的合作。构建产业生态需要更开放的合作研究和更广泛的行业实践，只有在产业链的共同努力推进下，才会促进IPv6的更快发展。为了推动IPv6的规模部署与发展，应该不断完善融合政策体系，为运营商和行业的合作渠道或载体提供便利，构建共赢的产业生态。</p>

        <h3>7 结束语</h3>

        <p>5G、云计算的到来，进一步驱动高带宽和低时延业务的发展，物联网、工业互联网带动了海量IP地址需求，这些新业务使得规模部署IPv6成为必然，也将为IPv6产业创造重要机遇。IPv6的根本目的是服务于整个网络，加速各个行业从IPv4向IPv6转型，所以需要加强顶层设计，营造有利的政策环境以促进产业链上中下游各方的高效协作。同时，需要通过制定和完善技术与产业标准，推广成功案例经验，引导和加快IPv6在行业的落地应用和服务创新。</p>

