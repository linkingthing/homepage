        <p>在各分支机构部署数字资产治理分平台，与集团总平台进行联动，可以向集团总平台进行地址申请，可以根据申请到的地址前缀基于整体地址规划方案进行二次地址规划及分配，负责分支机构基础设施、业务系统、终端等进行地址分发、管理、追踪及监测，负责分支机构域名解析，负责定时上报分支机构数字资产治理分平台相关管理数据、业务数据到集团公司总部。</p>

        <h3>5 IPv6地址管理要求</h3>

        <p>大型企业IPv6地址分配建议遵循“统一规划，分批启用”的原则。企业集团可以根据各业务发展规划，前期以5年为周期对各企业IPv6地址需求进行增量趋势预测及预规划。各二级企业在规划周期内根据网络建设和业务发展需求，向企业集团申请IPv6地址，并完成企业内部的IPv6地址使用规划，建议规划方案向集团报备。在5年规划周期内，原则上不再向各企业追加IPv6地址规划，期间个别企业因业务发展导致地址需求超出预规划量，可向集团提出IPv6地址申请，集团对申请进行审核、批复。</p>

        <p>5.1 地址申请、分配管理要求</p>
        <p>大型企业建议制定本集团和二级企业的地址申请、分配流程。各企业在IPv6地址利用率超过70％或剩余可用IPv6地址无法满足网络扩容或业务发展需求时，可向企业集团提出地址申请，每次申请的IPv6地址数量应保证其5年的使用增量。</p>

        <p>企业也应建立技术化手段，采用数字产治理的理念，建设IPv6地址动态监控、实时分析、综合管理的技术平台，作为企业网络地址管理的有效技术手段。</p>

        <p>5.2 地址使用、报备工作建议
        <p>建议企业建立集团和二级企业的地址规划、分配和使用规定。同时，为有效控制网间及互联网路由发布，避免无穿透服务需求的设备、业务、用户地址路由发布至互联网，建议将有穿透需求的设备、业务、用户的IPv6地址以“/64”为最小汇聚单位进行独立规划[5]。</p>

        <p>企业也应通过技术手段收集网络地址分配、使用率和路由发布情况，了解掌握IPv6地址在企业区域中心或对应广域网络节点的归属、业务、应用、穿透性等属性。</p>

        <h3>6 结束语</h3>

        <p>2017年11月26日，中共中央办公厅、国务院办公厅联合印发了《推进互联网协议第六版（IPv6）规模部署行动计划》，推进IPv6 规模部署是互联网技术产业生态的一次全面升级，深刻影响着网络信息技术、产业、应用的创新和变革。IPv6规模部署是一个复杂的系统工程，在IPv6规模部署的一个关键痛点在于海量稀疏模式的IPv6地址规划、分配、管理、访问难题。本文围绕以IPv6地址为核心的企业数字资产管理，从流程化、可视化、规划化的角度，给出了大型央企IPv6地址从规划设计到分配、管理、追踪、回收的全生命周期管理最佳实践，帮助企业实现平滑的IPv6演进升级，提升企业整体运营效率。</p>
