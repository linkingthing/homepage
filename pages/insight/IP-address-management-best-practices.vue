        <p>IP 地址是网络上每个虚拟或物理对象的唯一标识，IP 地址范围定义了这些对象的自身网络。在企业网络中，对IP基础结构和更多IP地址的需求比以往任何时候都大。
        </p>

        <p>混合云或者多云基础架构、移动设备的激增、IoT 海量终端以及 IPv6 的出现给组织带来越来越大的压力，它们要求简化配置和管理大量 IP 地址的方式，同时更有效地监督 DHCP 活动。另外，现实世界中的组织存在多个网络环境，而且不同网络环境中不同的网络对象分配 IP 地址的方式不同，比如传统的主机通过 DHCP 进行地址分配和管理；容器调度平台通过 CNI 插件调用 IPAM 插件获取单独的 IP 地址空间来实现 IP 地址分配和设置容器所需的路由。在跨越多个不同网络构建复杂的应用程序时，需要跟踪在数据中心内和数据中心外的多个不同网络环境中创建的所有 IP 地址和子网。
        </p>
         
        <p>分配可路由并遵循组织策略的 IP 地址的能力变得越来越重要。不同网络平台没有可见性来管理组成不同网络多个网络对象之间的 IP 地址，跨多个网络或者主机的重叠 IP 地址的危险呈指数增长。但是，由于不同网络的 IPAM 流程之间没有实现统一的工具集成，从而导致管理的不一致，同时限制自动化并妨碍审核和安全合规性。因此，需要在整个组织中以统一的方式管理不同网络环境下的 IP 地址管理。
        </p>

